package lesson_2;

public class SwitchCase {

  public static void main(String[] args) {

    int a = 5;

    switch (a) {
      case 1:
        System.out.println("Hello world 5");
        break;
      case 5:
        System.out.println("Hello world 2");
        break;
      case 3:
        System.out.println("Hello world 3");
        break;
      default:
        System.out.println("Unknown");
    }

  }

}
