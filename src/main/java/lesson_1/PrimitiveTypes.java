package lesson_1;

public class PrimitiveTypes {

  public static void main(String[] args) {

     /*
      Java primitive types
     */

    // byte - 1 byte
    byte a = 100;

    // short - 2 byte
    short b = 400;

    // int - 4 byte
    int c = 19500;

    // long - 8 byte
    long d = 1000000000L;

    // float - 4 byte
    float e = 100.05F;

    // double - 8 byte
    double f = 17418451845.1581551;

    // char (character)
    char g = 'a';

    // boolean
    boolean h = false;
    boolean i = true;

    /*
      type casting
     */
    byte a1 = 120;
    int a2 = a1;
    byte b1 = (byte) a2; // we loosing data




  }

}
